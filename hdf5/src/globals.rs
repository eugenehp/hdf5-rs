//! Global constants (API-compatible with the FFI crate's `globals` module).
//!
//! The FFI crate exposes lazily-initialized identifiers of predefined
//! datatypes, property-list classes and error classes registered inside the
//! C library. In the pure-Rust implementation these identifiers have no
//! backing registry; the same names are provided as distinct synthetic
//! constants so that downstream code referring to `hdf5::globals::*` (and
//! dereferencing them with `*`) keeps compiling. They are accepted nowhere in
//! the pure-Rust API, mirroring their `#[doc(hidden)]` status upstream.

use std::ops::Deref;

use crate::h5i::hid_t;

/// A global constant identifier (dereferences to [`hid_t`]).
pub struct H5GlobalConstant(hid_t);

impl Deref for H5GlobalConstant {
    type Target = hid_t;

    fn deref(&self) -> &hid_t {
        &self.0
    }
}

